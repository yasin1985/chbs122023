class CHBShortcode
{
	/**************************************************************************/
	
	function __construct()
	{

	}
	
	/**************************************************************************/
	
	static function create($argument,$name='booking_form')
	{
		$shortcodeArgument=null;
		if(is_array($argument))
		{
			foreach($argument as $argumentIndex=>$argumentValue)
				$shortcodeArgument.=' '.$argumentIndex.'="'.$argumentValue.'"';
		}
		
		$shortcode='['.PLUGIN_CHBS_CONTEXT.'_'.$name.$shortcodeArgument.']';
		
		return($shortcode);
	}
	
	/**************************************************************************/
}

