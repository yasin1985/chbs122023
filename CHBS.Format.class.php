class CHBSFormat
{
	/**************************************************************************/
	
	static function toSaveFloat($value,$empty=false,$decimalNumberCount=2)
	{
		$Validation=new CHBSValidation();
		
		if(($Validation->isEmpty($value)) && ($empty)) return('');
		
		$value=preg_replace('/,/','.',$value);
		$value=number_format($value,$decimalNumberCount,'.','');
		return($value);
	}
	
	/**************************************************************************/
}

