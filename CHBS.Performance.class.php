class CHBSPerformance
{
	/**************************************************************************/
	
	static function start()
	{
		global $chbs_performance_timer;
		$chbs_performance_timer=microtime(true); 
	}
	
	/**************************************************************************/
	
	static function stop()
	{
		global $chbs_performance_timer;
		
		$time=microtime(true)-$chbs_performance_timer;
		
		echo sprintf(esc_html__('Total time: %s','chauffeur-booking-system'),$time).'<br>';
	}
	
	/**************************************************************************/
}

