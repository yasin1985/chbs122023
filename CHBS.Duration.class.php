class CHBSDuration
{
    /**************************************************************************/
    
    function __construct()
    {
        
    }
    
    /**************************************************************************/
    
    function format($value)
    {
        $value=floor($value/60).__(' h','chauffeur-booking-system').' '.($value%60).__(' m','chauffeur-booking-system');
        return($value);
    }

    /**************************************************************************/
}

