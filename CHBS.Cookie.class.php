class CHBSCookie
{
	/**************************************************************************/
	
	static function set($name,$value)
	{
		$name=PLUGIN_CHBS_CONTEXT.'_'.$name;
		
		setcookie($name,$value);
		$_COOKIE[$name]=$value;
	}
	
	/**************************************************************************/
	
	static function get($name)
	{
		$name=PLUGIN_CHBS_CONTEXT.'_'.$name;
		
		if(!is_array($_COOKIE)) return(null);
		if(!array_key_exists($name,$_COOKIE)) return(null);
		
		return($_COOKIE[$name]);
	}

	/**************************************************************************/
}

