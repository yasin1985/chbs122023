class CHBSPriceType
{
	/**************************************************************************/
	
	function __construct()
	{
		$this->priceType=array
		(
			1=>array(__('Variable','chauffeur-booking-system')),
			2=>array(__('Fixed','chauffeur-booking-system'))
		);
	}
	
	/**************************************************************************/
	
	function getPriceType()
	{
		return($this->priceType);
	}

	/**************************************************************************/
	
	function getPriceTypeName($priceType)
	{
		if(!$this->isPriceType($priceType)) return(null);
		return($this->priceType[$priceType][0]);
	}
	
	/**************************************************************************/
	
	function isPriceType($priceType)
	{
		return(array_key_exists($priceType,$this->priceType));
	}
	
	/**************************************************************************/
}

